function drawVertex({ x, y }) {
  const size = 10
  const centered_x = x - (size / 2)
  const centerd_y = y - (size / 2)
  ctx.fillStyle = "green"
  ctx.fillRect(centered_x, centerd_y, size, size)
}

function drawLine(p1, p2) {
  ctx.strokeStyle = "green"
  ctx.beginPath()
  ctx.moveTo(p1.x, p1.y)
  ctx.lineTo(p2.x, p2.y)
  ctx.stroke()
}


function denormalizedPoint({ x, y }) {
  return {
    x: ((1 + x) * screen.width / 2),
    y: ((1 - y) * screen.height / 2)
  }
}

function clearScreen() {
  ctx.fillStyle = "black"
  ctx.fillRect(0, 0, screen.width, screen.height)
}

function translateZ(point, delta) {
  return {
    x: point.x,
    y: point.y,
    z: point.z + delta
  }
}

function projectPoint({ x, y, z }) {
  return {
    x: x / z,
    y: y / z
  }

}

const FPS = 60

function rotatePoint(point, angle) {
  return {
    x: point.x * Math.cos(angle) - point.z * Math.sin(angle),
    y: point.y,
    z: point.x * Math.sin(angle) + point.z * Math.cos(angle),
  }
}


function drawShape(shapeFaces, shapeVertices, deltaZ) {
  for (let i = 0; i < shapeFaces.length; i++) {
    for (let j = 0; j < shapeFaces[i].length; j++) {
      const indexP1 = shapeFaces[i][j] - 1  // Blender indices start at 1
      const indexP2 = shapeFaces[i][(j + 1) % shapeFaces[i].length] - 1
      p1 = denormalizedPoint(projectPoint(translateZ(shapeVertices[indexP1], deltaZ)))
      p2 = denormalizedPoint(projectPoint(translateZ(shapeVertices[indexP2], deltaZ)))

      drawLine(p1, p2)

    }
  }
}

function rotateShape(shapeVertices, angle) {
  for (let i = 0; i < shapeVertices.length; i++) {
    shapeVertices[i] = rotatePoint(shapeVertices[i], angle / FPS)
  }
}
